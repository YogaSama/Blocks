package fr.creatruth.blocks.manager.item.type.liquid;

import fr.creatruth.blocks.manager.Materials;
import fr.creatruth.blocks.manager.item.BaseItem;
import fr.creatruth.blocks.manager.item.SpecialBase;
import org.bukkit.Material;
import org.bukkit.event.block.Action;
import org.bukkit.inventory.ItemStack;

abstract class LiquidItem extends BaseItem implements SpecialBase {

    Material    texture;
    short       durabilility;

    public LiquidItem(ItemStack item, Materials materials) {
        super(item, materials);
    }

    @Override
    public BaseItem getSpecialBase(byte data) {
        BaseItem baseItem = specialItemBuilder(texture, getItemBuilder().getMaterial(), data);
        blockDurability(baseItem);
        return baseItem;
    }

    @Override
    public void onSwitch(Action action) {
        super.onSwitch(action);
        blockDurability(this);
    }

    void blockDurability(BaseItem baseItem) {
        baseItem.getItem().setDurability(durabilility);
    }
}
